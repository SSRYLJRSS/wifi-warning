  t(key, vars = {}) {
    const table = this.dict[this.lang] || this.dict["zh-CN"];
    let value = table[key] || this.dict["zh-CN"][key] || key;
    for (const [name, replacement] of Object.entries(vars)) {
      value = value.replaceAll(`{${name}}`, String(replacement));
    }
    return value;
  },
  setLanguage(lang) {
    this.lang = lang === "en-US" ? "en-US" : "zh-CN";
    document.documentElement.lang = this.lang;
    document.querySelectorAll("[data-i18n]").forEach((node) => {
      node.textContent = this.t(node.dataset.i18n);
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((node) => {
      node.setAttribute("placeholder", this.t(node.dataset.i18nPlaceholder));
    });
    document.querySelectorAll("[data-i18n-title]").forEach((node) => {
      node.setAttribute("title", this.t(node.dataset.i18nTitle));
    });
    document.querySelectorAll("[data-lang]").forEach((node) => {
      node.classList.toggle("active", node.dataset.lang === this.lang);
    });
    const titleKey = document.title === "WiFi Warning" || document.title === "WiFi 提醒" ? "appName" : "";
    if (titleKey) document.title = this.t(titleKey);
  }
